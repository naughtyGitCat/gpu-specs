def get_filtered_models(params: dict[str, str] = None) -> list[dict[str, str]]:
    url = "https://www.techpowerup.com/gpu-specs/"
    r = requests.get(url, params=params)
    soup = BeautifulSoup(r.text, 'html.parser')
    table = soup.find('table', attrs={'class': 'processors'})
    thead = table.find('thead', {'class': 'colheader'})
    keys: list[str] = list()
    tr = thead.find('tr')
    for th in tr.find_all('th'):
        keys.append(th.text)
    for tr in table.findAll('tr'):
        if tr.parent.name == 'thead':
            continue
        values: list[str] = list()
        for td in tr.findAll('td'):
            relative_path = td.find('a')
            if relative_path is not None:
                values.append(f"{td.text.strip()}##{relative_path.attrs['href']}")
            else:
                values.append(td.text.strip())
        yield dict(zip(keys, values))


